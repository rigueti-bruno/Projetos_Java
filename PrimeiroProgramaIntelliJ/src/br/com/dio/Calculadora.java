package br.com.dio;

public class Calculadora {
    public static void main(String[] args) {
        // Variáveis
        int a = 6;
        int b = 3;

        // Operações
        int soma = a + b;
        int sub = a - b;
        int mult = a * b;
        int div = a / b;
        System.out.println(soma);
        System.out.println(sub);
        System.out.println(mult);
        System.out.println(div);
    }
}